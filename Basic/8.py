'''
8. First and Last Colors

Write a Python program to display the first and last colors from the following list.
color_list = ["Red","Green","White" ,"Black"]
'''

# color list
color_list = ["Red","Green","White" ,"Black"]
f_color = color_list[0]
l_color = color_list[-1]

