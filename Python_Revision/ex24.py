# Dictionary

about = {'name': 'Ray', 'age':45, 'occupation': 'Engineer'}

print(about['name'])
print(about['age'])
print(about['occupation'])

about["Height"] = "6.4 Feet"
print(about)
# returns 
# {'name': 'Ray', 'age': 45, 'occupation': 'Engineer', 'Height': '6.4 Feet'}

about["Address"] = "10 Street, Club Columbia, New Mexico"
print(about)