# Strings and Text

# f-strings
total_bulls = 18
print(f"There are {total_bulls} bulls")

# Formatting Strings with .format() method
name = "Miguel Prado"
age = "39"
formatted_string = "Hi! I am {} and I am {} years old.".format(name, age)
print(formatted_string)

# Other way to use format method by directly adding in print function
print("Hey I am {} and I am {} years old.".format(name,age))

# String Concatenation
print("I am " + name + "and I am " + age + " years old")